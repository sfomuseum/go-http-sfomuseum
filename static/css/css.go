// package css provides Cascading Style Sheet (CSS) assets for use in SFO Museum web applications.
// CSS files with a `drupal` suffix are sourced from the sfomuseum.org website.
// CSS files with a `pruned` suffix are modified versions of their `drupal` counterparts and contain only
// those elements necessary for functionality in the common SFO Museum header and footer.
// The `sfomuseum.org.bootstrap.css` file provides overrides to the default Bootstrap CSS rules to ensure
// that the common SFO Museum header and footer CSS rules are preserved.
// The `sfomuseum.sweater.css` file is the unminified concatenation of all the relevant CSS files necessary
// for the common SFO Museum header and footers to function. It is generated programatically in the Makefile
// at the root level of this package.
